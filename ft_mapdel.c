#include "libft.h"

void			ft_mapdel(char **map)
{
	int			i;

	i = -1;
	if (map)
	{
		while (map[++i])
		{
			free(map[i]);
			map[i] = NULL;
		}
		free(map);
		map = NULL;
	}
}
